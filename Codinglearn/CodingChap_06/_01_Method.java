package Study.Codinglearn.CodingChap_06;

public class _01_Method {

    public static void sayHello() {
        System.out.println("sayHello 메소드 내 println 코드입니다.");
    }
    
    public static void main(String[] args) {
        sayHello();  // 메소드 호출
    }
}

